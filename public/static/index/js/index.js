$(function(){
    //搜索导航栏
    $('.search-best-item').mouseover(function(){
        $(this).find('.search-best-content').css('height','230px');
        $('.search-best-content').css('transition','0.3s');
    }).mouseout(function(){
        $(this).find('.search-best-content').css('height','0');
        $('.search-best-content').css('transition','0.3s');
    });
})